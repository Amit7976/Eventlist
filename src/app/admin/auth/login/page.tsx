import { auth } from '@/nextAuth/auth';
import { redirect } from 'next/navigation';
import MainContent from './MainContent';


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


const page = async () => {


  ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


  const session = await auth();
  if (session?.user) {
    redirect("/admin")
  }


  ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


  return (
    <>
      <div className='bg-white dark:bg-neutral-900'>
        <MainContent />
     </div>
    </>
  )
}

export default page